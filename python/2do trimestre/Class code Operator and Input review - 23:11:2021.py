edad=int(input("Insert your age: "))
edadMeses=edad*12
print("You have been alive for",edadMeses,"months")
edadSemanas=edadMeses*4
print("You have been alive for",edadSemanas,"weeks")
edadDias=edadSemanas*7
print("You have been alive for",edadDias,"days")
edadHoras=edadDias*24
print("You have been alive for",edadHoras,"hours")
edadMinutos=edadHoras*60
print("You have been alive for",edadMinutos,"minutes")
edadSegundos=edadMinutos*60
print("You have been alive for",edadSegundos,"seconds")
edadMilesimaDeSegundos=edadSegundos*1000
print("You have been alive for",edadMilesimaDeSegundos,"thousandths of a second")